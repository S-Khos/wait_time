    def fit(self, data) -> int:
        data = np.array(data)
        self.buffer.append(data)
        # Filter valid indices
        valid_indices = (
            (0 <= data[:, 0])
            & (data[:, 0] < self.frame_width)
            & (0 <= data[:, 1])
            & (data[:, 1] < self.frame_height)
        )
        # Set the valid indices in the matrix to 1
        self.matrix[data[valid_indices, 0], data[valid_indices, 1]] = 1
        visited = np.full(self.matrix.shape, False, dtype=bool)
        label_count = 2
        label_coordinates = {}
        for i in range(len(self.matrix)):
            for j in range(len(self.matrix[0])):
                if self.matrix[i][j] == 1 and not visited[i][j]:
                    coordinates = (
                        []
                    )  # List to store the coordinates of the current component
                    self.dfs(visited, i, j, label_count, coordinates)
                    if len(coordinates) < 25:
                        # If the component has fewer than 20 elements, set all its elements to 0 (anomaly removal)
                        for x, y in coordinates:
                            self.matrix[x][y] = 0
                    else:
                        # Label the component
                        for x, y in coordinates:
                            self.matrix[x][y] = label_count
                        label_coordinates[label_count] = (
                            coordinates  # Store the coordinates for this label
                        )
                        label_count += 1  # Increment label for the next component
        return label_count - 1

    def predict(self, centroids):
        """
        figure out a more optimized search algrithm (opportunistic and attention)

        """
        centroids = np.array(centroids)
        labels = np.full(len(centroids), -1)  # Initialize labels with default value -1
        padded_matrix = np.pad(
            self.matrix,
            pad_width=self.label_search_radius,
            mode="constant",
            constant_values=0,
        )
        for idx, (x, y) in enumerate(centroids):
            if 0 <= x < self.frame_width and 0 <= y < self.frame_height:
                # Convert coordinates to account for padding
                px, py = x + self.label_search_radius, y + self.label_search_radius
                neighborhood = padded_matrix[
                    px - self.label_search_radius : px + self.label_search_radius,
                    py - self.label_search_radius : py + self.label_search_radius,
                ]
                neighbor_labels = neighborhood[neighborhood != 0]
                if neighbor_labels.size > 0:
                    most_common_label = Counter(neighbor_labels).most_common(1)[0][0]
                    labels[idx] = int(most_common_label) - 1

        return labels


