    def __init__(
        self,
        frame=None,
        connected_search_radius=70,
        label_search_radius=80,
        anomaly_tol=25,
    ) -> None:
        self.frame_width = frame[0]
        self.frame_height = frame[1]
        self.matrix = np.zeros((self.frame_width, self.frame_height), dtype=int)
        self.connected_search_radius = connected_search_radius
        self.label_search_radius = label_search_radius
        self.anomaly_tol = anomaly_tol
        self.buffer = FixedBuffer(max_len=70)
        self.output_matrix = None

    def dfs(self, visited, i, j, label, coordinates) -> None:
        stack = [(i, j)]
        visited[i, j] = True
        coordinates.append((i, j))
        while stack:
            x, y = stack.pop()
            # Create a grid of offsets
            dx, dy = np.meshgrid(np.arange(-self.connected_search_radius, self.connected_search_radius + 1), np.arange(-self.connected_search_radius, self.connected_search_radius + 1))
            dx, dy = dx.flatten(), dy.flatten()
            # Remove the zero offset
            valid_offsets = (dx != 0) | (dy != 0)
            dx, dy = dx[valid_offsets], dy[valid_offsets]

            # Calculate neighbor coordinates
            neighbors_x = x + dx
            neighbors_y = y + dy

            # Filter valid neighbor coordinates
            valid_neighbors = (
                (0 <= neighbors_x) & (neighbors_x < self.matrix.shape[0]) &
                (0 <= neighbors_y) & (neighbors_y < self.matrix.shape[1])
            )

            # Apply the boundary check before accessing the arrays
            neighbors_x = neighbors_x[valid_neighbors]
            neighbors_y = neighbors_y[valid_neighbors]

            valid_neighbors = (
                ~visited[neighbors_x, neighbors_y] &
                (self.matrix[neighbors_x, neighbors_y] == 1)
            )

            # Update visited and coordinates
            valid_neighbors_x = neighbors_x[valid_neighbors]
            valid_neighbors_y = neighbors_y[valid_neighbors]

            visited[valid_neighbors_x, valid_neighbors_y] = True
            coordinates.extend(zip(valid_neighbors_x, valid_neighbors_y))
            stack.extend(zip(valid_neighbors_x, valid_neighbors_y))

